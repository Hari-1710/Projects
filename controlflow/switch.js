const month = 3 //change with string also
switch (month)  {
    case 1: //here also change to string
        console.log("January");
        break;
    case 2:
        console.log("February");
        break;
    case 3:
        console.log("March");
        break;  
    case 4:
        console.log("April");
        break;

    default:
        console.log("Not a valid month");
        break;
}